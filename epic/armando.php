		<h1>Persona</h1>

		<p><strong>Name:</strong> Armando</p>

		<p><strong>Age:</strong> 44</p>

		<p><strong>Profession:</strong> Armando is Albuquerque's 2nd district representative. His district is
			one of the most populated areas in the city and covers most of the downtown-commercial area.</p>

		<p><strong>Technology:</strong> Armando uses a city-issued Android for work related communications and
			a iPhone for personal communication. Likewise, his office is equipped with a Windows 10 machine but he uses a
			Macbook in his leisure. Although limited in his choice of ISP's, Armando employs the lesser of the two evils
			as his provider, Comcast.</p>

		<p><strong>Attitudes and Behaviors:</strong> Armando is native Burqueno with a green-chile-like passion for his
			hometown. This passion fuels his career choice as well as his work ethic. Although his credentials could land
			him a higher government position, he prefers his council-ship as it allows him to work directly with his
			constituents.</p>


		<p><strong>Frustrations and Needs:</strong> Armando is constantly juggling requests from businesses and his
			constituents. He has a PR analyst Gene who he uses sparingly to help with communications when he has too much
			on his plate. Constrained by the city's dwindling budget, Armando is constantly searching for smarter solutions
			to satisfy his district's requests.</p>


		<p><strong>Goals:</strong> Armando is always curious how his policies and statements are interpreted by his
			constituents. He hosts bi-monthly town halls but the hours aren't accessible to a large portion of his
			constituents. Ideally, Armando would have a communication tool that works with everybody schedule so he
			effectively address the issues important to his community.</p>


		<p><strong>User Story:</strong> One of the most arduous aspects of my job is determining how my constituents
			interpret my policies; some reveal this sentiment through social media but I find it can be a bit
			biased. I need a forum all of them could access and leave their questions and criticisms for me.</p>

		<p><strong>Use Case and Interactions:</strong> Armando receives an email from the staff at ABQTownHall with a link
			to setup an account. He clicks on the link.
			The site loads the landing page. He is intrigued by the mission statement and decides to click the sign-up
			button
			titled "we want you." The site responds by loading a create profile page. He begins to fill in the relevant
			profile
			information including his first name, last name, username, address, and email. He clicks submit profile. Upon
			clicking submit,
			the site responds with a popup that says he has an email confirmation message on the way and that he will need
			to confirm.
			An auto generated email is sent to him. He checks his email and clicks on the hyperlink to confirm his email
			which redirects him to a personal landing page on the site.
			The site loads confirming his new account. From his personal landing page, he sees all of Albuquerque's nine
			City Council Districts and the Mayoral forums.
			As he clicks on each, the site responds with a message stating he has view-only permissions in each district he
			does not represent and read/write permissions in his district and the Mayoral forum. He decides to click on his
			district's forum,
			then clicks create post, types a post, and submits to let his constituents know he is now on the forum. The
			site responds by loading a create post page and then saving and posting his input. After posting,
			he decides to review a few other posts and respond to questions. When responding, the the site loads a create
			response page and then saving and posting his input.</p>
