			unlimited data plan. She uses Chrome browser because of its cross-platform support and ease of use.</p>
		<p><strong>Attitudes and Behaviors:</strong> Jenny is a college graduate and intellectually curious. She works in
			marketing and has a new-found personal interest in politics and activism. She is actively looking for ways she
			can become more involved in local politics and connect with like-minded individuals.</p>
		<p><strong>Frustrations and Needs:</strong> Jenny is frustrated by how hard it to find information about city
			politics and link-up with others for civil dialog. She needs a user friendly site that provides the information
			regarding city politics and a platform to discuss issues with her peers and maybe communicate directly with
			city politicians.</p>
		<p><strong>Goals:</strong> Jenny wants to find a trustworthy site that clearly conveys the information about the
			city government she needs. She is comfortable with moderately complex websites that clearly state the relevant
			information.</p>
		<p><strong>Story:</strong> I'm looking for a website where I can pose questions to my city council member and
			interact with like-minded constituents.</p>
		<p><strong>Use Case and Interactions:</strong> As she surfs the web, Jenny comes across a Google link to the ABQ
			Town Hall website. The site loads the landing page. She is intrigued by the mission statement and decides to
			click the sign-up button titled "we want you." The site responds by loading a create profile page. She begins
			to fill in the relevant profile information including her first name, last name, username, address, and email.
			She clicks submit profile. Upon clicking submit, the site responds with a popup that says she has an email
			confirmation message on the way and that she will need to confirm. An auto generated email is sent to her. She
			checks her email and clicks on the hyperlink to confirm her email which redirects her to a personal landing
			page on the site. The site loads confirming her new account. From her personal landing page, she sees all of
			Albuquerque's nine City Council Districts and the Mayoral forums. As she clicks on each, the site responds with
			a message stating she has view-only permissions in each district she does not belong and read/write permissions
			in her district and the Mayoral forum. She decides to click on her district's forum, then clicks create post,
			types a post, and submits. The site responds by loading a create post page and then saving and posting her
			input. After posting, she decides to review a few other posts. As she reviews them she decides to up vote and
			down vote them based on she feels about the post. As she up votes or down votes, the site adds or subtracts her
			vote to the total for that post.</p>
